// Function to create cube vertices with color
inline void createCube(std::vector<float>& vertices, float r, float g, float b)
{
    for (int i = 0; i < 36; i++) {
        vertices.push_back(cubeVerticesData[i * 3]);
        vertices.push_back(cubeVerticesData[i * 3 + 1]);
        vertices.push_back(cubeVerticesData[i * 3 + 2]);
        vertices.push_back(r);
        vertices.push_back(g);
        vertices.push_back(b);
    }
}

